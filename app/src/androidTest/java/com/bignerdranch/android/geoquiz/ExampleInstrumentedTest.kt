/**
 * Instrumented test, which will execute on an Android device.
 *
 * See [testing documentation](http://d.android.com/tools/testing).
 */
@RunWith(AndroidJUnit4::class)
class ExampleInstrumentedTest {

    @get:Rule
    var mainActivityActivityTestRule = ActivityTestRule(MainActivity::class.java)


    @Test
    fun useAppContext() {
        // Context of the app under test.
        val appContext = InstrumentationRegistry.getInstrumentation().targetContext
        assertEquals("com.bignerdranch.android.geoquiz", appContext.packageName)
    }


    @Test
    fun shouldToastAfterClickingTrueButton() {

        onView(withId(R.id.false_button)).perform(click())

        onView(withId(R.id.false_button)).check(matches(isDisplayed()))

    }


}
